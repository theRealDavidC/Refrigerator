// Low-level I2C helpers 
static void oled_send_cmd(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };
    HAL_I2C_Master_Transmit(&OLED_I2C_HANDLE, OLED_I2C_ADDR, buf, 2, 10);
}

static void oled_send_data(const uint8_t *data, uint16_t len)
{
    // Prepend the data-stream control byte 0x40 
    uint8_t header = 0x40;
    HAL_I2C_Master_Transmit(&OLED_I2C_HANDLE, OLED_I2C_ADDR, &header, 1, 5);
    HAL_I2C_Master_Transmit(&OLED_I2C_HANDLE, OLED_I2C_ADDR, (uint8_t *)data, len, 50);
}

// Public API

void ssd1306_init(void)
{
    HAL_Delay(10);

    const uint8_t init_seq[] = {
        0xAE,       // display off                  
        0x20, 0x00, // horizontal addressing mode   
        0xB0,       // page start = 0               
        0xC8,       // COM scan direction           
        0x00,       // low col offset               
        0x10,       // high col offset              
        0x40,       // display start line = 0       
        0x81, 0xFF, // max contrast                 
        0xA1,       // segment remap                
        0xA6,       // normal display (not inverted)
        0xA8, 0x3F, // multiplex ratio 64           
        0xA4,       // output follows RAM           
        0xD3, 0x00, // display offset = 0           
        0xD5, 0xF0, // clock divide / osc freq      
        0xD9, 0x22, // pre-charge period            
        0xDA, 0x12, // COM pins config              
        0xDB, 0x20, // VCOMH deselect level         
        0x8D, 0x14, // charge pump enable           
        0xAF        // display on                   
    };

    for (uint8_t i = 0; i < sizeof(init_seq); i++) {
        oled_send_cmd(init_seq[i]);
    }

    ssd1306_clear();
    ssd1306_flush();
}

void ssd1306_clear(void)
{
    memset(framebuf, 0, sizeof(framebuf));
}

void ssd1306_flush(void)
{
    for (uint8_t page = 0; page < PAGES; page++) {
        oled_send_cmd(0xB0 + page);
        oled_send_cmd(0x00);
        oled_send_cmd(0x10);
        oled_send_data(framebuf[page], OLED_WIDTH_PX);
    }
}

void ssd1306_draw_char(uint8_t x, uint8_t y, char ch, ssd1306_color_t color)
{
    if (ch < 0x20 || ch > 0x7A) ch = '?';
    const uint8_t *glyph = font5x7[ch - 0x20];
    uint8_t page = y / 8;
    uint8_t bit  = y % 8;

    if (page >= PAGES) return;

    for (uint8_t col = 0; col < 5 && (x + col) < OLED_WIDTH_PX; col++) {
        uint8_t line = glyph[col];
        if (color == SSD1306_COLOR_WHITE) {
            framebuf[page][x + col] |= (line << bit);
        } else {
            framebuf[page][x + col] &= ~(line << bit);
        }
    }
}

void ssd1306_draw_string(uint8_t x, uint8_t y, const char *str, ssd1306_color_t color)
{
    while (*str) {
        ssd1306_draw_char(x, y, *str++, color);
        x += 6;
        if (x + 6 > OLED_WIDTH_PX) break;
    }
}

void ssd1306_draw_line_h(uint8_t x, uint8_t y, uint8_t len, ssd1306_color_t color)
{
    uint8_t page = y / 8;
    uint8_t bit  = 1 << (y % 8);
    if (page >= PAGES) return;
    for (uint8_t i = 0; i < len && (x + i) < OLED_WIDTH_PX; i++) {
        if (color == SSD1306_COLOR_WHITE) {
            framebuf[page][x + i] |= bit;
        } else {
            framebuf[page][x + i] &= ~bit;
        }
    }
}

void ssd1306_fill_rect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, ssd1306_color_t color)
{
    for (uint8_t row = y; row < y + h; row++) {
        ssd1306_draw_line_h(x, row, w, color);
    }
}
